def add_two_numbers(num_one, num_two):
    number_to_return = num_one + num_two
    return number_to_return


def multiply_two_numbers(num_one, num_two):
    number_to_return = num_one * num_two
    return number_to_return


def divide_two_numbers(num_one, num_two):
    number_to_return = num_one / num_two
    return number_to_return 


def subtract_two_numbers(num_one, num_two):
    number_to_return = num_one - num_two
    return number_to_return


